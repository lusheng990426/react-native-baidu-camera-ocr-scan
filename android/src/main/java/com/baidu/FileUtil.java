    public static String getimage(String srcPath, File newFile) {
        BitmapFactory.Options newOpts = new BitmapFactory.Options();
        //开始读入图片，此时把options.inJustDecodeBounds 设回true了
        newOpts.inJustDecodeBounds = true;
        Bitmap bitmap = BitmapFactory.decodeFile(srcPath,newOpts);//此时返回bm为空

        newOpts.inJustDecodeBounds = false;
        int w = newOpts.outWidth;
        int h = newOpts.outHeight;
        //现在主流手机比较多是800*480分辨率，所以高和宽我们设置为
        float hh = 800f;//这里设置高度为800f
        float ww = 480f;//这里设置宽度为480f
        //缩放比。由于是固定比例缩放，只用高或者宽其中一个数据进行计算即可
        int be = 1;//be=1表示不缩放
        if (w > h && w > ww) {//如果宽度大的话根据宽度固定大小缩放
            be = (int) (newOpts.outWidth / ww);
        } else if (w < h && h > hh) {//如果高度高的话根据宽度固定大小缩放
            be = (int) (newOpts.outHeight / hh);
        }
        if (be <= 0)
            be = 1;
        newOpts.inSampleSize = be;//设置缩放比例
        //重新读入图片，注意此时已经把options.inJustDecodeBounds 设回false了
        bitmap = BitmapFactory.decodeFile(srcPath, newOpts);
        return compressImage(bitmap,srcPath,newFile);//压缩好比例大小后再进行质量压缩
    }

    public static String compressImage(Bitmap image, String path, File newFile) {


        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        image.compress(Bitmap.CompressFormat.JPEG, 100, baos);//质量压缩方法，这里100表示不压缩，把压缩后的数据存放到baos中
        ByteArrayInputStream isBm = new ByteArrayInputStream(baos.toByteArray());//把压缩后的数据baos存放到ByteArrayInputStream中
        Bitmap bitmap = BitmapFactory.decodeStream(isBm);

        int orientation = FileUtil.readPictureDegree(path);//获取旋转角度
        if(Math.abs(orientation) > 0){
            bitmap =  FileUtil.rotaingImageView(orientation, bitmap);//旋转图片
        }

        int quality = 100;
        int is_one_diff = 0;
        do {

            try {
                FileOutputStream fos = new FileOutputStream(newFile);
                bitmap.compress(Bitmap.CompressFormat.JPEG, quality, fos);
                fos.flush();
                fos.close();

                Log.d("momo",String.valueOf(newFile.length()));

            }catch (Exception e){

            }finally {

                if(is_one_diff == 0){
                    is_one_diff = 1;
                    if((newFile.length() / 1024) >= 4000){
                        quality -= 80;
                    }else if((newFile.length() / 1024) >= 3000){
                        quality -= 70;
                    }else if((newFile.length() / 1024) >= 2000){
                        quality -= 60;
                    }else if((newFile.length() / 1024) >= 1000){
                        quality -= 50;
                    }

                }else{
                    quality -= 5;
                }


            }

        }while ((newFile.length() / 1024) > 2000);

        return newFile.getPath();

    }


